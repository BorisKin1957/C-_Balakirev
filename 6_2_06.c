/*  Известно, что среднее арифметическое от m чисел можно вычислять рекуррентно
по формуле:

Qm = (m - 1) / m * Qm-1 + x / m
​
  - среднее арифметическое от предыдущих m-1 чисел; x - новое m-е значение.

В программе ниже необходимо реализовать подсчет среднего арифметического
читаемых целых чисел из входного потока. Для этого объявите функцию со следующей
сигнатурой:

double get_qm(double qm_1, int m, int x);
Функция get_qm должна вычислять очередное значение среднего арифметического для
m прочитанных чисел и возвращать вычисленное значение Qm. В записанном цикле
while с помощью функции get_qm вычислите среднее арифметическое всех прочитанных
целых чисел из входного потока. Выведите результат вычисления в консоль в виде
вещественного числа с точностью до тысячных. */

#include <stdio.h>

/* Функция рекуррентного вычисления среднего арифметического */
double get_qm(double qm_1, int m, int x) {
    if (m == 1)
        return x; // Для первого числа среднее равно самому числу
    return ((m - 1) * qm_1 + x) / m; // Применяем формулу
}

int main(void) {
    int x;
    int m = 1; // Счётчик количества прочитанных чисел
    double qm_1 = 0; // Текущее среднее значение

    while (scanf("%d", &x) == 1) {
        // Обновляем среднее значение с помощью рекуррентной формулы
        qm_1 = get_qm(qm_1, m, x);
        m++; // Увеличиваем счётчик
    }

    printf("%.3f\n", qm_1);

    return 0;
}