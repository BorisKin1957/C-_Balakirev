/* Напишите программу, в которой с каждой новой строки вводятся названия
городов. Пустая строка должна быть проигнорирована. Максимум может быть введено
10 городов. Максимальная длина названия не превышает 50 символов. Сформировать
строку, в которой названия городов идут по порядку (считывания) через пробел без
дублирования, то есть, повторяющиеся названия отбрасывать. Вывести
сформированную строку в консоль.

Тесты: https://github.com/selfedu-rus/c-tests/tree/main/5.10.7

Sample Input:

Барнаул
Кишинев
Ярославль
Николаев

Уфа
Москва
Санкт-Петербург
Николаев
Самара
Воронеж

Смоленск
 */

#include <stdio.h>
#include <string.h>

#define MAX_CITIES 10 // Максимальное количество городов
#define MAX_NAME_LENGTH 50 // Максимальная длина названия города

int main() {

    char cities[MAX_CITIES *
                (MAX_NAME_LENGTH + 1)]; // Массив для хранения городов
    int city_count = 0; // Счетчик количества прочитанных городов
    char buffer[MAX_NAME_LENGTH + 1]; // Буфер для чтения строки

    // Чтение до конца ввода (EOF)
    while (fgets(buffer, MAX_NAME_LENGTH, stdin) != NULL &&
           city_count < MAX_CITIES) {
        // Удаляем символы новой строки
        size_t len = strlen(buffer);
        if (len > 0 && buffer[len - 1] == '\n') {
            buffer[len - 1] = '\0'; // заменяем символ новой строки на ноль
            len--;
        }

        // Формируем результирующую строку, пропуская пустые строки
        // и города из массива cities
        if (len > 0) {
            if (strstr(cities, buffer) == NULL) {
                strcat(cities, buffer);
                if (city_count < MAX_CITIES - 1)
                    strcat(cities, " "); // Добавляем пробел
            }
            city_count++;
        }
    }

    // Вывод результата
    puts(cities);

    return 0;
}