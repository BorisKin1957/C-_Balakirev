
/*  Напишите программу, в которой объявляется перечисление без идентификатора и
константами:

_do=1, _re=2, _mi=3, _fa=4, _sol=5, _la=6, _si=7

Определите тип этого перечисления как NOTES. Объявите функцию с именем
get_major, которая имеет один параметр с типом NOTES. Функция должна возвращать
1, если были переданы значения констант: _do, или _mi, или _sol. В противном
случае функция get_major возвращает 0.

В функции main прочитайте из входного потока пять целых чисел в диапазоне [1;
7], записанных в одну строчку через пробел. Выведите в консоль (в одну строчку
через пробел) только те числа, для которых функция get_major вернула истинное
значение (единицу).

Тесты: https://github.com/selfedu-rus/c-tests/tree/main/7.5.8

Sample Input:

1 2 3 2 5
Sample Output:

1 3 5
 */


#include <stdio.h>
#include <stdlib.h>

// Определяем перечисление и тип NOTES
typedef enum {
    _do = 1,
    _re = 2,
    _mi = 3,
    _fa = 4,
    _sol = 5,
    _la = 6,
    _si = 7
} NOTES;

// Объявляем функцию
int get_major(NOTES n);

int main(void) {
    int num; // Используем int для ввода
    NOTES *numbers = malloc(5 * sizeof(NOTES)); // Выделяем память под 5 элементов

    // if (numbers == NULL) {
    //     return 1; // Проверка на ошибку выделения памяти
    // }

    // Чтение 5 чисел из входного потока
    for (size_t i = 0; i < 5 && scanf("%d", &num) == 1; i++) {
        numbers[i] = (NOTES)num; // Преобразуем int в NOTES
    }

    // Выводим только те числа, для которых get_major вернула 1
    for (size_t i = 0; i < 5; i++) {
        if (get_major(numbers[i])) {
            printf("%d ", (int)numbers[i]); // Приводим к int для корректного вывода
        }
    }
    printf("\n"); // Добавляем перевод строки в конце

    free(numbers); // Освобождаем память
    
    return 0;
}

// Реализация функции get_major
int get_major(NOTES n) {
    return (n == _do || n == _mi || n == _sol);
}